import streamlit as st
import pandas as pd

def main():
    # Title of the app
    st.title("Bulk Prediction Feature")

    # Description and instructions for users
    st.write("""
    ### How to Use the Bulk Prediction Feature

    1. **Download the Test Data**:
       Click the "Download Income Prediction Test Data" button below to get a sample CSV file with the required format for bulk predictions.

    2. **Prepare Your Data**:
       Open the downloaded CSV file and replace the sample data with your own data. Ensure that your data matches the format and column names expected by the model.

    3. **Upload Your Data**:
       Use the upload section (not included in this snippet but should be part of your app) to upload your prepared CSV file.

    4. **Run Predictions**:
       Once your file is uploaded, click on the "Run Predictions" button (also not included in this snippet but should be part of your app) to process your data. The app will generate predictions based on the data in your CSV file.

    5. **Download Results**:
       After processing, you can download the predictions along with any additional results or insights generated by the model.

    Feel free to reach out if you have any questions or need further assistance!
    """)

    # Load the CSV file into a DataFrame
    file_path = 'Data/Income Prediction Test Data.csv'
    df = pd.read_csv(file_path)

    # Convert DataFrame to CSV for download
    csv = df.to_csv(index=False)

    # Create a download button
    st.download_button(
        label="Download Income Prediction Test Data",
        data=csv,
        file_name='Income_Prediction_Test_Data.csv',
        mime='text/csv'
    )

if __name__ == "__main__":
    main()
